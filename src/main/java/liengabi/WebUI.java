    @Step("Open URL: {0}")
    public static void openURL(String url) {
        DriverManager.getDriver().get(url);
        DriverManager.updateWindowHandles();
        DriverManager.updateOriginalWindowHandle();
        sleep(STEP_TIME);
        LogUtils.info("Open: " + url);
        ExtentTestManager.logMessage(Status.INFO, "Open URL: " + url);
    }

    @Step("Click element: {0}")
    public static void clickElement(By by) {
        waitForElementClickable(by);
        highLightElement(by);
        sleep(STEP_TIME);
        DriverManager.getDriver().findElement(by).click();
        DriverManager.updateWindowHandles();
        LogUtils.info("Click element: " + by);
        ExtentTestManager.logMessage(Status.INFO, "Click element " + by);
    }

